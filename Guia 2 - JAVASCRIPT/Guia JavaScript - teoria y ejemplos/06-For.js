// Bucle (loop) for.
// atencion a los operadores. usar == o === o !== para mas estrictez en los tipos de datos

// for
for (let i = 0; i < 10; i++) {
  console.log("El valor de [i] es: " + i);
}

// forIn
for (const key in object) {
  console.log(key);
}

// forof
for (let first of second) {
  console.log(first);
}

// foreach method. (arrays)
array.forEach((element) => {});
